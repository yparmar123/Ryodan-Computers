let items = window.itemsData;

let buildItemList = function(items, category) {
    let list = document.querySelector('#items');
    let title = document.createElement('h2');
    title.innerHTML = `Displaying: ${category}`;
    list.appendChild(title);
    items.forEach(element => {
        let nl = document.createElement('br');
        let item = document.createElement('div');
        item.className = 'item';
        let img = document.createElement('img');
        img.src = element.image;
        img.className = element.rotate;
        img.alt = '';
        img.width = '175';
        let name = document.createElement('h2');
        name.innerHTML = element.itemName;
        let desc = document.createElement('span');
        desc.class = 'description';
        desc.innerHTML = `${element.description}<br>`;
        desc.appendChild(nl);
        let category = document.createElement('span');
        category.class = 'category';
        category.innerHTML = `Category: ${element.category}<br><br>`;
        let price = document.createElement('span');
        price.class = 'price';
        price.innerHTML = element.price;
        item.appendChild(img);
        item.appendChild(name);
        item.appendChild(desc);
        item.appendChild(category);
        item.appendChild(price);
        list.appendChild(item);
    });
}; 

let clearItems = function() {
    let item = document.querySelector('#items');
    item.innerHTML = ' ';
};

let setupMenuHandlers = function() {
    let all = document.querySelector('#all');
    let comp = document.querySelector('#components');
    let acc = document.querySelector('#Accessories');
    let allClick = function() {
        clearItems();
        buildItemList(items, "All Items");
    };
    let compClick = function() {
        let arr = getByCategory('Component');
        clearItems();
        buildItemList(arr, "Components");
    };
    let accClick = function() {
        let arr = getByCategory('Accessory');
        clearItems();
        buildItemList(arr, "Accessories");
    };

    all.addEventListener('click', allClick);
    comp.addEventListener('click', compClick);
    acc.addEventListener('click', accClick);
};

let getByCategory = function(category){
    let arr = [];
    arr = items.filter(element => element.category === category);
    return arr;
};

window.onload = function(){  
    buildItemList(items, "All Items");
    setupMenuHandlers(); 
};

